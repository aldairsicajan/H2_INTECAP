public class VariablesenJava {

    public static void main(String[] args) {
        //variable byte
        byte entero = 12; //entero es el nombre de la variable
        
        System.out.println("GUATEMALA: "+entero);
        
        short entero2 = 12456;
        System.out.println("GUATEMALA: "+entero2);
        
        float decimal = 3.1416f;
        System.out.println("FLOAT: " +decimal);
        
        char caracter = '5';
        System.out.println("DUDA: " +caracter);
    }
}
